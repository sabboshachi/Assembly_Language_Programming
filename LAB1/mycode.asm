.MODEL SMALL                                           
.STACK 100H
.CODE
MAIN PROC

MOV AH,1    ;INPUT
INT 21H

MOV BL,AL   ;MOVING BL TO AL

MOV AH,2    ; 2ND FUNCTION FOR OUTPUT

MOV DL,0DH ; CARRIAGE RETURN
INT 21H

MOV DL,0AH; LINE FEED
INT 21H

MOV DL,BL   ;DISPLAY
INT 21H   

MOV AH,4CH
INT 21H

MAIN ENDP
END MAIN
